public class Person {
    // Instance variables
    String name;
    String address;

    // Constructor
    Person(String name, String address) {
        this.name = name;
        this.address = address;
    }

    // Getter methods
    String getName() {
        return name;
    }
    String getAddress() {
        return name;
    }
    // Setter methods
    void setAddress(String newAddress) {
        address = newAddress;
    }
}
